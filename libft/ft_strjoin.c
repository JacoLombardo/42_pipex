#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(char));
	if (!str)
		return (NULL);
	i = ft_strlcpy(str, s1, ft_strlen(s1) + 1);
	ft_strlcpy(str + i, s2, ft_strlen(s2) + 1);
	return (str);
}
